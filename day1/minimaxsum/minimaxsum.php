function miniMaxSum($arr) {
    // Write your code here
    sort($arr);
    echo array_sum(array_slice($arr, 0, 4)) . ' ' . array_sum(array_slice($arr, -4)) . "\n";
}

$arr_temp = rtrim(fgets(STDIN));

$arr = array_map('intval', preg_split('/ /', $arr_temp, -1, PREG_SPLIT_NO_EMPTY));

miniMaxSum($arr);
